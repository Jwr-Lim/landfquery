ALTER PROC USP_POP_004_RESULT_NEW_COMBO_ORDER
(
     @DIV_CD      NVARCHAR(10) = '01' 
    ,@PLANT_CD    NVARCHAR(10) = '1130'
    ,@WC_CD       NVARCHAR(10) = '13GA'
    ,@LINE_CD     NVARCHAR(10) = '13G02A' 
    ,@PROC_CD     NVARCHAR(10) = 'RK' 
)
AS



SELECT A.REVISION AS CODE, A.ORDER_NO AS NAME
    FROM PD_ORDER A WITH (NOLOCK) 
    INNER JOIN PD_ORDER_PROC B WITH (NOLOCK) ON A.DIV_CD = B.DIV_CD AND A.PLANT_CD = B.PLANT_CD AND A.ORDER_NO = B.ORDER_NO AND A.REVISION = B.REVISION 
    AND A.ORDER_TYPE = B.ORDER_TYPE AND A.ORDER_FORM = B.ORDER_FORM AND A.ROUT_NO = B.ROUT_NO AND A.ROUT_VER = B.ROUT_VER AND A.WC_CD = B.WC_CD AND A.LINE_CD = B.LINE_CD 
    AND B.PROC_CD = @PROC_CD AND B.S_CHK = 'Y' 
WHERE A.DIV_CD = @DIV_CD AND A.PLANT_CD = @PLANT_CD AND A.WC_CD = @WC_CD AND A.LINE_CD = @LINE_CD AND A.CLOSE_FLG = 'N'